package com.suziye.TheaterSightSharingWeb.Theater;

public class Theater {
	private String theater_id;
	private String mapping_data;
	private String theater_lat;
	private String theater_lon;
	private String address;
	private int seats;
	
	
	public String getTheater_id() {
		return theater_id;
	}
	public void setTheater_id(String theater_id) {
		this.theater_id = theater_id;
	}
	public String getMapping_data() {
		return mapping_data;
	}
	public void setMapping_data(String mapping_data) {
		this.mapping_data = mapping_data;
	}
	public String getTheater_lat() {
		return theater_lat;
	}
	public void setTheater_lat(String theater_lat) {
		this.theater_lat = theater_lat;
	}
	public String getTheater_lon() {
		return theater_lon;
	}
	public void setTheater_lon(String theater_lon) {
		this.theater_lon = theater_lon;
	}
	public String getAddress() {
		return address;
	}
	public void setAddress(String address) {
		this.address = address;
	}
	public int getSeats() {
		return seats;
	}
	public void setSeats(int seats) {
		this.seats = seats;
	}
	
	

}
